#En Python, una lista de listas es simplemente una lista que contiene otras listas como sus elementos. Esta estructura permite crear matrices o tablas bidimensionales, donde cada sublista representa una fila de la matriz.
#Ejemplo de Lista de Listas

#Supongamos que queremos representar una matriz 3x3. Podemos hacerlo utilizando una lista de listas:

matriz = [[1,2,3],
        [4,5,6],
        [7,8,9]]

#En este ejemplo, matriz es una lista que contiene tres sublistas. Cada sublista representa una fila de la matriz.
